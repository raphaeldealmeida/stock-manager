<template>
    <v-data-table
            :headers="headers"
            :items="products"
            :items-per-page="5"
            item-key="id"
    >
        <template v-slot:top>
            <v-toolbar
                    flat
            >
                <v-dialog
                        v-model="dialog"
                        max-width="500px"
                >
                    <template v-slot:activator="{ on, attrs }">
                        <v-btn
                                color="primary"
                                dark
                                class="mb-2"
                                v-bind="attrs"
                                v-on="on"
                        >
                            New Product
                        </v-btn>
                    </template>
                    <v-card>
                        <v-card-text>
                            <v-container>
                                <v-row>
                                    <v-col
                                            cols="12"

                                    >
                                        <v-text-field
                                                v-model="editedItem.name"
                                                label="Name"
                                        ></v-text-field>
                                    </v-col>
                                </v-row>
                                <v-row>
                                    <v-col
                                            cols="6"
                                    >
                                        <v-text-field
                                                v-model="editedItem.price"
                                                label="Price"
                                        ></v-text-field>
                                    </v-col>
                                    <v-col
                                            cols="6"
                                    >
                                        <v-text-field
                                                v-model="editedItem.quantity"
                                                label="Quantity"
                                        ></v-text-field>
                                    </v-col>
                                </v-row>
                            </v-container>
                        </v-card-text>
                        <v-card-actions>
                            <v-spacer></v-spacer>
                            <v-btn
                                    color="blue darken-1"
                                    text
                                    @click="close"
                            >
                                Cancel
                            </v-btn>
                            <v-btn
                                    color="blue darken-1"
                                    text
                                    @click="save"
                            >
                                Save
                            </v-btn>
                        </v-card-actions>
                    </v-card>
                </v-dialog>
                <v-dialog v-model="dialogDelete" max-width="500px">
                    <v-card>
                        <v-card-title class="headline">Are you sure you want to delete this item?</v-card-title>
                        <v-card-actions>
                            <v-spacer></v-spacer>
                            <v-btn color="blue darken-1" text @click="closeDelete">Cancel</v-btn>
                            <v-btn color="blue darken-1" text @click="deleteItemConfirm">OK</v-btn>
                            <v-spacer></v-spacer>
                        </v-card-actions>
                    </v-card>
                </v-dialog>
            </v-toolbar>
        </template>
        <template v-slot:item.actions="{ item }">
            <v-icon
                    small
                    class="mr-2"
                    @click="editItem(item)"
            >
                mdi-pencil
            </v-icon>
            <v-icon
                    small
                    @click="deleteItem(item)"
            >
                mdi-delete
            </v-icon>
        </template>
    </v-data-table>
</template>

<script>
    export default {
        name: "ProductTable",
        props: {
            products: Array,
        },
        data: () => ({
                headers: [
                    {text: 'ID', value: 'id'},
                    {text: 'Name', value: 'name', align: 'start'},
                    {text: 'Price (U$)', value: 'price'},
                    {text: 'Quantity (UN)', value: 'current_quantity'},
                    {text: 'Actions', value: 'actions', sortable: false},
                ],
                editedItem: {
                    id: 0,
                    name: '',
                    price: 0.0,
                    current_quantity: 0,
                },
                defaultItem: {
                    id: 0,
                    name: '',
                    price: 0.0,
                    current_quantity: 0,
                },
                dialog: false,
                dialogDelete: false,
                editedIndex: -1,
        }),
        watch: {
          dialog (val) {
            val || this.close()
          },
          dialogDelete (val) {
            val || this.closeDelete()
          },
        },
        methods: {
            editItem (item) {
                this.editedIndex = this.products.indexOf(item)
                this.editedItem = Object.assign({}, item)
                this.dialog = true
            },
            deleteItem (item) {
                this.editedIndex = this.products.indexOf(item)
                this.editedItem = Object.assign({}, item)
                this.dialogDelete = true
            },
            deleteItemConfirm () {
              this.products.splice(this.editedIndex, 1)
              this.closeDelete()
            },
            close () {
              this.dialog = false
              this.$nextTick(() => {
                this.editedItem = Object.assign({}, this.defaultItem)
                this.editedIndex = -1
              })
            },
            closeDelete () {
              this.dialogDelete = false
              this.$nextTick(() => {
                this.editedItem = Object.assign({}, this.defaultItem)
                this.editedIndex = -1
              })
            },
            save () {
              if (this.editedIndex > -1) {
                Object.assign(this.products[this.editedIndex], this.editedItem)
              } else {
                this.products.push(this.editedItem)
              }
              this.close()
            },
        }
    }
</script>

